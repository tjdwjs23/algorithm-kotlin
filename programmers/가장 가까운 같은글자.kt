/**
 * 문제 설명
 * 문자열 s가 주어졌을 때, s의 각 위치마다 자신보다 앞에 나왔으면서, 자신과 가장 가까운 곳에 있는 같은 글자가 어디 있는지 알고 싶습니다.
 * 예를 들어, s="banana"라고 할 때,  각 글자들을 왼쪽부터 오른쪽으로 읽어 나가면서 다음과 같이 진행할 수 있습니다.
 *
 * b는 처음 나왔기 때문에 자신의 앞에 같은 글자가 없습니다. 이는 -1로 표현합니다.
 * a는 처음 나왔기 때문에 자신의 앞에 같은 글자가 없습니다. 이는 -1로 표현합니다.
 * n은 처음 나왔기 때문에 자신의 앞에 같은 글자가 없습니다. 이는 -1로 표현합니다.
 * a는 자신보다 두 칸 앞에 a가 있습니다. 이는 2로 표현합니다.
 * n도 자신보다 두 칸 앞에 n이 있습니다. 이는 2로 표현합니다.
 * a는 자신보다 두 칸, 네 칸 앞에 a가 있습니다. 이 중 가까운 것은 두 칸 앞이고, 이는 2로 표현합니다.
 * 따라서 최종 결과물은 [-1, -1, -1, 2, 2, 2]가 됩니다.
 *
 * 문자열 s이 주어질 때, 위와 같이 정의된 연산을 수행하는 함수 solution을 완성해주세요.
 */

class Solution {
    fun solution(s: String): IntArray {
        // 1. 결과를 저장할 정수 배열 생성 (초기값은 모두 -1)
        val result = IntArray(s.length) { -1 }

        // 2. 각 문자가 마지막으로 등장한 인덱스를 기억할 Map 생성
        // - Key: 문자(Char), Value: 인덱스(Int)
        val lastSeen = mutableMapOf<Char, Int>()

        // 3. 문자열의 처음부터 끝까지 인덱스(i)를 돌며 순회
        for (i in s.indices) {
            val char = s[i] // 현재 확인 중인 문자

            // 4. 현재 문자가 Map에 존재하는지 확인 (이전에 나온 적이 있는지)
            if (lastSeen.containsKey(char)) {
                // 이전에 나온 적이 있다면:
                // (현재 인덱스 - 마지막으로 등장한 인덱스) = 거리를 계산하여 결과 배열에 저장
                result[i] = i - lastSeen[char]!!
            }

            // 5. 현재 문자의 위치정보를 Map에 업데이트 (가장 최근 위치로 갱신)
            lastSeen[char] = i
        }

        // 6. 완성된 결과 배열 반환
        return result
    }
}